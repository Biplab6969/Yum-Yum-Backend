// @desc    Update user (Admin only)
// @route   PUT /api/auth/users/:id
// @access  Private/Admin
exports.updateUser = async (req, res) => {
  try {
    const { name, email, role, shopId, isActive } = req.body;

    const user = await User.findById(req.params.id);
    if (!user) {
      return res.status(404).json({
        success: false,
        message: 'User not found'
      });
    }

    const oldValue = { name: user.name, email: user.email, role: user.role, isActive: user.isActive };

    // Update fields
    if (name) user.name = name;
    if (email) user.email = email;
    if (role) user.role = role;
    if (shopId !== undefined) user.shopId = shopId;
    if (isActive !== undefined) user.isActive = isActive;

    await user.save();

    // Log action
    await AuditLog.logAction({
      userId: req.user._id,
      action: 'UPDATE_USER',
      entityType: 'User',
      entityId: user._id,
      description: `Updated user: ${user.name}`,
      oldValue,
      newValue: { name: user.name, email: user.email, role: user.role, isActive: user.isActive },
      ipAddress: req.ip
    });

    res.status(200).json({
      success: true,
      message: 'User updated successfully',
      data: user
    });
  } catch (error) {
    res.status(500).json({
      success: false,
      message: 'Error updating user',
      error: error.message
    });
  }
};

// @desc    Delete user (Admin only)
// @route   DELETE /api/auth/users/:id
// @access  Private/Admin
exports.deleteUser = async (req, res) => {
  try {
    const user = await User.findById(req.params.id);

    if (!user) {
      return res.status(404).json({
        success: false,
        message: 'User not found'
      });
    }

    // Don't allow deleting yourself
    if (user._id.toString() === req.user._id.toString()) {
      return res.status(400).json({
        success: false,
        message: 'Cannot delete your own account'
      });
    }

    await User.findByIdAndDelete(req.params.id);

    // Log action
    await AuditLog.logAction({
      userId: req.user._id,
      action: 'DELETE_USER',
      entityType: 'User',
      entityId: user._id,
      description: `Deleted user: ${user.name}`,
      oldValue: { name: user.name, email: user.email, role: user.role },
      ipAddress: req.ip
    });

    res.status(200).json({
      success: true,
      message: 'User deleted successfully'
    });
  } catch (error) {
    res.status(500).json({
      success: false,
      message: 'Error deleting user',
      error: error.message
    });
  }
};
